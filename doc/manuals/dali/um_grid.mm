.LI
GRID: Toggle grids.
.P
Via this command the display grid and the snap grid can be displayed
according to the settings as controlled via the \fIdisp_grid\fP
and \fIsnap_grid\fP commands (settings menu).
A grid is displayed only if it has been switched on.
By subsequently selecting the \fIgrid\fP command the display
of the grids is alternately turned on and off: toggle grids.
If the display of the grids is turned on,
the display grid spacing is reported in the TEXT viewport.
